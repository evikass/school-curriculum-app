def get_lesson_titles(subject_dir):
    """Extract lesson titles from existing SVGs."""
    titles = {}
    if not os.path.isdir(subject_dir):
        return titles
    for f in sorted(os.listdir(subject_dir)):
        if not f.endswith('.svg'):
            continue
        path = os.path.join(subject_dir, f)
        with open(path, 'r', encoding='utf-8') as fh:
            content = fh.read()
        # Extract title from the bold text element
        import re
        match = re.search(r'font-weight="bold" fill="[^"]*" text-anchor="middle">([^<]+)</text>', content)
        if match:
            lesson_num = int(re.search(r'lesson(\d+)', f).group(1))
            titles[lesson_num] = match.group(1)
    return titles


