def svg_footer(title, c, decoration=""):
    """Standard footer with title panel and optional decoration."""
    deco = f'\n  {decoration}' if decoration else ''
    return f'''  <!-- Bottom panel -->
  <rect x="15" y="308" width="470" height="30" rx="4" fill="{c['primary']}" opacity="0.85"/>
  <text x="250" y="329" font-family="Arial,sans-serif" font-size="14" text-anchor="middle" fill="white" font-weight="bold">{title}</text>{deco}
</svg>'''

def info_card(x, y, w, h, title, content, border_color, title_bg=True):
    """Create an info card with colored header bar and content."""
    parts = [f'  <rect x="{x}" y="{y}" width="{w}" height="{h}" rx="6" fill="white" stroke="{border_color}" stroke-width="1.5" opacity="0.95"/>']
    if title_bg:
        parts.append(f'  <rect x="{x}" y="{y}" width="{w}" height="24" rx="6" fill="{border_color}" opacity="0.9"/>')
        parts.append(f'  <rect x="{x}" y="{y+12}" width="{w}" height="12" fill="{border_color}" opacity="0.9"/>')
        parts.append(f'  <text x="{x+w//2}" y="{y+17}" font-family="Arial,sans-serif" font-size="11" font-weight="bold" fill="white" text-anchor="middle">{title}</text>')
    else:
        parts.append(f'  <text x="{x+w//2}" y="{y+15}" font-family="Arial,sans-serif" font-size="10" fill="{border_color}" text-anchor="middle" font-weight="bold">{title}</text>')
        parts.append(f'  <line x1="{x+8}" y1="{y+20}" x2="{x+w-8}" y2="{y+20}" stroke="#E0E0E0" stroke-width="1"/>')
    parts.append(content)
    return '\n'.join(parts)

def label(x, y, text, color="#555", size=9, anchor="middle", bold=False):
    w = "bold" if bold else "normal"
    return f'<text x="{x}" y="{y}" font-family="Arial,sans-serif" font-size="{size}" fill="{color}" text-anchor="{anchor}" font-weight="{w}">{text}</text>'

def box(x, y, w, h, text, fill_color, text_color="white", radius=3):
    return f'<rect x="{x}" y="{y}" width="{w}" height="{h}" rx="{radius}" fill="{fill_color}" opacity="0.15"/>\n<text x="{x+w//2}" y="{y+h//2+4}" font-family="Arial,sans-serif" font-size="9" fill="{fill_color}" text-anchor="middle" font-weight="bold">{text}</text>'

def arrow_down(x, y1, y2, color="#888"):
    return f'<line x1="{x}" y1="{y1}" x2="{x}" y2="{y2}" stroke="{color}" stroke-width="1.5"/><polygon points="{x-4},{y2-6} {x+4},{y2-6} {x},{y2}" fill="{color}"/>'

def arrow_right(x1, x2, y, color="#888"):
    return f'<line x1="{x1}" y1="{y}" x2="{x2}" y2="{y}" stroke="{color}" stroke-width="1.5"/><polygon points="{x2-6},{y-4} {x2-6},{y+4} {x2},{y}" fill="{color}"/>'

