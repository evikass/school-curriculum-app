def card(x, y, w, h, accent=ACCENT1):
    return f'''<rect x="{x}" y="{y}" width="{w}" height="{h}" rx="6" fill="{CARD_BG}" stroke="{accent}" stroke-width="1" filter="url(#sh)"/>'''

def text_item(x, y, text, color=TEXT, size=10, bold=False):
    weight = ' font-weight="bold"' if bold else ''
    return f'<text x="{x}" y="{y}" font-family="Arial,sans-serif" font-size="{size}"{weight} fill="{color}">{text}</text>'

def bullet(x, y, color=ACCENT1):
    return f'<circle cx="{x}" cy="{y-3}" r="3" fill="{color}"/>'

