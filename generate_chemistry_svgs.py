def generate_svg(lesson_num, title, sections):
    svg = header(title)
    
    # Calculate layout
    num_sections = len(sections)
    if num_sections == 3:
        # 2 cards on top, 1 on bottom
        positions = [
            (10, 58, 225, 140),
            (245, 58, 225, 140),
            (10, 206, 460, 140),
        ]
    elif num_sections == 2:
        positions = [
            (10, 58, 230, 290),
            (250, 58, 220, 290),
        ]
    else:
        positions = [
            (10, 58, 460, 290),
        ]
    
    for i, (section_title, accent, items) in enumerate(sections):
        if i >= len(positions):
            break
        x, y, w, h = positions[i]
        svg += '\n  ' + card(x, y, w, h, accent)
        svg += '\n  ' + text_item(x + 12, y + 18, section_title, accent, 11, True)
        
        # Separator line
        svg += f'\n  <line x1="{x+12}" y1="{y+24}" x2="{x+w-12}" y2="{y+24}" stroke="{accent}" stroke-width="0.5" opacity="0.5"/>'
        
        for j, item in enumerate(items):
            item_y = y + 40 + j * 22
            if item_y > y + h - 8:
                break
            svg += '\n  ' + bullet(x + 16, item_y, accent)
            
            # Truncate long text
            display_text = item if len(item) <= 35 else item[:33] + "…"
            svg += '\n  ' + text_item(x + 26, item_y, display_text, TEXT, 9.5)
    
    # Lesson number badge
    svg += f'\n  <rect x="420" y="330" width="50" height="22" rx="11" fill="{ACCENT1}" opacity="0.3"/>'
    svg += f'\n  <text x="445" y="345" text-anchor="middle" font-family="Arial,sans-serif" font-size="10" fill="{ACCENT1}">#{lesson_num}</text>'
    
    svg += '\n</svg>'
    return svg

# Generate all SVGs
for i, (title, sections) in enumerate(lessons, 1):
    svg_content = generate_svg(i, title, sections)
    filepath = os.path.join(OUTPUT_DIR, f"lesson{i}.svg")
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(svg_content)
    print(f"Generated lesson{i}.svg: {title}")

print(f"\nDone! Generated {len(lessons)} SVG files")
