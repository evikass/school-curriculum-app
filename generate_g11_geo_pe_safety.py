def hex_to_rgb(h):
    h = h.lstrip("#")
    return int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)


def el(parent, tag, text=None, **kw):
    """Create an SVG sub-element with attribute dict and optional text."""
    e = ET.SubElement(parent, tag, **kw)
    if text is not None:
        e.text = text
    return e


def make_svg(cfg, lesson_num, lesson_data):
    title, bullets = lesson_data
    n = lesson_num
    primary = cfg["primary"]
    bg = cfg["bg"]
    header_text = cfg["header_tpl"].format(n=n)
    footer_text = cfg["footer"]
    icon_sym = cfg["icon_sym"]

    pr, pg, pb = hex_to_rgb(primary)
    primary_light = f"rgba({pr},{pg},{pb},0.12)"
    primary_mid = f"rgba({pr},{pg},{pb},0.25)"
    lighter = f"#{min(pr+40,255):02x}{min(pg+40,255):02x}{min(pb+40,255):02x}"

    W, H = 500, 350
    svg = ET.Element("svg")
    svg.set("xmlns", "http://www.w3.org/2000/svg")
    svg.set("viewBox", f"0 0 {W} {H}")
    svg.set("width", str(W))
    svg.set("height", str(H))

    # defs
    defs = el(svg, "defs")
    grad = el(defs, "linearGradient", id="hdr", x1="0", y1="0", x2="1", y2="1")
    el(grad, "stop", offset="0%", **{"stop-color": primary})
    el(grad, "stop", offset="100%", **{"stop-color": lighter})
    filt = el(defs, "filter", id="sh", x="-5%", y="-5%", width="110%", height="110%")
    el(filt, "feDropShadow", dx="0", dy="2", stdDeviation="4", **{"flood-color": "rgba(0,0,0,0.10)"})

    # background
    el(svg, "rect", width=str(W), height=str(H), rx="12", ry="12", fill=bg)
    el(svg, "rect", x="1", y="1", width=str(W-2), height=str(H-2),
       rx="12", ry="12", fill="none", stroke=primary, **{"stroke-width": "2.5"})

    # header bar
    el(svg, "rect", x="16", y="16", width=str(W-32), height="54",
       rx="10", ry="10", fill="url(#hdr)", filter="url(#sh)")

    # header icon
    icon_el = el(svg, "text", x="32", y="50",
                 **{"font-family": "sans-serif", "font-size": "22", "fill": "#FFFFFF"})
    icon_el.text = icon_sym

    # header texts
    h1 = el(svg, "text", x="60", y="38",
            **{"font-family": "sans-serif", "font-weight": "700",
               "font-size": "14", "fill": "#FFFFFF"})
    h1.text = header_text
    h2 = el(svg, "text", x="60", y="56",
            **{"font-family": "sans-serif", "font-weight": "600",
               "font-size": "11", "fill": "rgba(255,255,255,0.85)"})
    h2.text = f"11 класс · Урок {n}"

    # lesson number badge
    bx, by = W - 50, 43
    el(svg, "circle", cx=str(bx), cy=str(by), r="20",
       fill="rgba(255,255,255,0.20)", stroke="rgba(255,255,255,0.50)",
       **{"stroke-width": "1.5"})
    bt = el(svg, "text", x=str(bx), y=str(by + 7),
            **{"font-family": "sans-serif", "font-weight": "800",
               "font-size": "18", "fill": "#FFFFFF", "text-anchor": "middle"})
    bt.text = str(n)

    # title
    tt = el(svg, "text", x="250", y="96",
            **{"font-family": "sans-serif", "font-weight": "700",
               "font-size": "17", "fill": primary, "text-anchor": "middle"})
    tt.text = title

    # title underline
    el(svg, "line", x1="80", y1="106", x2="420", y2="106",
       stroke=primary_mid, **{"stroke-width": "1.5", "stroke-dasharray": "6,3"})

    # bullet cards
    CY0, CH, CG, CX = 120, 42, 6, 32
    CW = W - 64
    for i, bullet in enumerate(bullets):
        cy = CY0 + i * (CH + CG)
        el(svg, "rect", x=str(CX), y=str(cy), width=str(CW), height=str(CH),
           rx="8", ry="8", fill=primary_light, stroke=primary_mid, **{"stroke-width": "1"})

        ncx, ncy = CX + 22, cy + CH // 2
        el(svg, "circle", cx=str(ncx), cy=str(ncy), r="13", fill=primary)
        nt = el(svg, "text", x=str(ncx), y=str(ncy + 5),
                **{"font-family": "sans-serif", "font-weight": "700",
                   "font-size": "12", "fill": "#FFFFFF", "text-anchor": "middle"})
        nt.text = str(i + 1)

        display = bullet if len(bullet) <= 48 else bullet[:45] + "..."
        dt = el(svg, "text", x=str(CX + 42), y=str(ncy + 5),
                **{"font-family": "sans-serif", "font-weight": "500",
                   "font-size": "13", "fill": "#263238"})
        dt.text = display

    # footer
    el(svg, "rect", x="16", y=str(H - 44), width=str(W - 32), height="28",
       rx="8", ry="8", fill="rgba(0,0,0,0.05)")
    ft = el(svg, "text", x="250", y=str(H - 26),
            **{"font-family": "sans-serif", "font-weight": "600",
               "font-size": "11", "fill": primary, "text-anchor": "middle"})
    ft.text = footer_text

    return svg


def main():
    total = 0
    errors = []
    for subj_key, cfg in SUBJECTS.items():
        out_dir = cfg["dir"]
        os.makedirs(out_dir, exist_ok=True)
        lessons = cfg["lessons"]
        print(f"\n📁 {subj_key.upper()} → {out_dir}")
        for idx, lesson_data in enumerate(lessons, 1):
            svg = make_svg(cfg, idx, lesson_data)
            path = os.path.join(out_dir, f"lesson{idx}.svg")
            tree = ET.ElementTree(svg)
            ET.indent(tree, space="  ")
            tree.write(path, encoding="unicode", xml_declaration=True)

            # validate
            try:
                ET.parse(path)
            except ET.ParseError as e:
                errors.append((path, str(e)))
                print(f"  ❌ lesson{idx}.svg — INVALID: {e}")
                continue

            total += 1
            print(f"  ✅ lesson{idx}.svg")

    print(f"\n{'='*60}")
    print(f"Generated and validated {total}/36 SVG files")
    if errors:
        print(f"ERRORS ({len(errors)}):")
        for p, e in errors:
            print(f"  {p}: {e}")
    if total == 36:
        print("ALL 36 FILES VALID ✓")
    print(f"{'='*60}")


if __name__ == "__main__":
    main()
