def main():
    # Read the JSON file
    with open(FILE_PATH, "r", encoding="utf-8") as f:
        data = json.load(f)

    lessons_updated = 0

    # Navigate to detailedTopics -> lessons
    for topic in data["lessons"]["detailedTopics"]:
        for lesson in topic["lessons"]:
            title = lesson["title"]
            # Find matching lesson data by keyword in title
            matched_key = None
            for key in LESSON_DATA:
                if key in title:
                    matched_key = key
                    break

            if matched_key:
                lesson_data = LESSON_DATA[matched_key]
                lesson["facts"] = lesson_data["facts"]
                lesson["tests"] = lesson_data["tests"]
                lessons_updated += 1
                print(f"  ✓ Updated: {title}")
            else:
                print(f"  ✗ No match: {title}")

    # Save back
    with open(FILE_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"\nTotal lessons updated: {lessons_updated}/8")


if __name__ == "__main__":
    main()
