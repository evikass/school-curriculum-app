def fix_lesson(lesson, extra_data, desc_ext=None, lang="ru"):
    """Fix a single lesson with missing content."""
    title = lesson["title"]
    
    # 1. Fix description (≥800 chars)
    if desc_ext and len(lesson["description"]) < 800:
        lesson["description"] += desc_ext
    
    # 2. Fix facts (3+)
    if "facts" not in lesson or not lesson["facts"]:
        lesson["facts"] = extra_data.get("facts", [])
    
    # 3. Fix tests
    if "tests" not in lesson or not lesson["tests"]:
        lesson["tests"] = {}
    
    tests = lesson["tests"]
    if "quiz" not in tests or not tests["quiz"]:
        tests["quiz"] = extra_data.get("quiz", [])
    if "find" not in tests or not tests["find"]:
        tests["find"] = extra_data.get("find", [])
    if "match" not in tests or not tests["match"]:
        tests["match"] = extra_data.get("match", [])
    
    # 4. Fix keyPoints (4+)
    extra_kp = extra_data.get("keyPoints_extra", [])
    while len(lesson["keyPoints"]) < 4 and extra_kp:
        kp = extra_kp.pop(0)
        if kp not in lesson["keyPoints"]:
            lesson["keyPoints"].append(kp)
    
    # 5. Fix examples (4+)
    extra_ex = extra_data.get("examples_extra", [])
    while len(lesson["examples"]) < 4 and extra_ex:
        ex = extra_ex.pop(0)
        if ex not in lesson["examples"]:
            lesson["examples"].append(ex)
    
    return lesson


def process_file(filepath, extra_map, desc_ext_map, lang="ru"):
    """Process a JSON file and fix all lessons."""
    with open(filepath, "r", encoding="utf-8") as f:
        data = json.load(f)
    
    total_lessons = 0
    fixed_lessons = 0
    
    for topic in data["lessons"]["detailedTopics"]:
        for i, lesson in enumerate(topic["lessons"]):
            total_lessons += 1
            title = lesson["title"]
            
            if title in extra_map:
                desc_ext = desc_ext_map.get(title) if desc_ext_map else None
                topic["lessons"][i] = fix_lesson(lesson, extra_map[title], desc_ext, lang)
                fixed_lessons += 1
            else:
                print(f"  WARNING: No extra data for lesson '{title}'")
    
    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    return total_lessons, fixed_lessons


def verify_file(filepath, min_desc_len=800, min_facts=3, min_kp=4, min_ex=4, min_quiz=5, min_find=5, min_match=5):
    """Verify all lessons meet requirements."""
    with open(filepath, "r", encoding="utf-8") as f:
        data = json.load(f)
    
    issues = []
    for topic in data["lessons"]["detailedTopics"]:
        for lesson in topic["lessons"]:
            title = lesson["title"]
            desc_len = len(lesson["description"])
            
            if desc_len < min_desc_len:
                issues.append(f"  {title}: description={desc_len} (min {min_desc_len})")
            
            facts = lesson.get("facts", [])
            if len(facts) < min_facts:
                issues.append(f"  {title}: facts={len(facts)} (min {min_facts})")
            
            kp = lesson.get("keyPoints", [])
            if len(kp) < min_kp:
                issues.append(f"  {title}: keyPoints={len(kp)} (min {min_kp})")
            
            ex = lesson.get("examples", [])
            if len(ex) < min_ex:
                issues.append(f"  {title}: examples={len(ex)} (min {min_ex})")
            
            tests = lesson.get("tests", {})
            quiz = tests.get("quiz", [])
            find = tests.get("find", [])
            match = tests.get("match", [])
            
            if len(quiz) < min_quiz:
                issues.append(f"  {title}: quiz={len(quiz)} (min {min_quiz})")
            if len(find) < min_find:
                issues.append(f"  {title}: find={len(find)} (min {min_find})")
            if len(match) < min_match:
                issues.append(f"  {title}: match={len(match)} (min {min_match})")
    
    return issues


