function getNextDistractor(pool, existingOptions, correctAnswer, usedFromPool) {
  for (const item of pool) {
    const key = item.toLowerCase().trim();
    if (usedFromPool.has(key)) continue;
    if (existingOptions.some(opt => opt.toLowerCase().trim() === key)) continue;
    if (correctAnswer && correctAnswer.toLowerCase().trim() === key) continue;
    usedFromPool.add(key);
    return item;
  }
  // Better fallback: combine pool items with prefixes to create more distractors
  const prefixes = ['Основы', 'Теория', 'Понятие', 'Свойство', 'Принцип', 'Закон', 'Правило', 'Метод', 'Вид', 'Тип', 'Форма', 'Элемент', 'Структура', 'Процесс', 'Явление', 'Система', 'Фактор', 'Условие', 'Признак', 'Характеристика'];
  for (const prefix of prefixes) {
    for (const item of pool) {
      const combined = `${prefix} ${item}`;
      const ck = combined.toLowerCase().trim();
      if (usedFromPool.has(ck)) continue;
      if (existingOptions.some(opt => opt.toLowerCase().trim() === ck)) continue;
      if (correctAnswer && correctAnswer.toLowerCase().trim() === ck) continue;
      usedFromPool.add(ck);
      return combined;
    }
  }
  return null;
}

function fixFile(filepath, grade, subject) {
  let content = fs.readFileSync(filepath, 'utf-8');
  const lines = content.split('\n');
  let replacementsMade = 0;
  const pool = POOLS[subject] || [];
  const usedFromPool = new Set();
  const skippedNoPool = [];
  
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    
    // Check if this line contains options:
    if (!line.includes('options:')) continue;
    
    // Extract all quoted strings from this line
    const optMatches = [...line.matchAll(/"([^"]*)"/g)];
    const fillersInLine = optMatches.filter(m => isFiller(m[1]));
    
    if (fillersInLine.length === 0) continue;
    
    // Extract correctAnswer from the same line or nearby
    let correctAnswer = null;
    const caMatch = line.match(/correctAnswer:\s*"([^"]*)"/);
    if (caMatch) correctAnswer = caMatch[1];
    
    // Get existing non-filler options for duplicate checking
    const existingOptions = optMatches.map(om => om[1]);
    
    // Replace filler strings in this line
    let newLine = line;
    for (const m of fillersInLine) {
      const filler = m[1];
      // Don't replace if it's the correctAnswer!
      if (correctAnswer && correctAnswer.toLowerCase().trim() === filler.toLowerCase().trim()) {
        console.log(`    SKIP (is correctAnswer): "${filler}"`);
        continue;
      }
      const replacement = getNextDistractor(pool, existingOptions, correctAnswer, usedFromPool);
      if (replacement) {
        newLine = newLine.replace(`"${filler}"`, `"${replacement}"`);
        replacementsMade++;
        console.log(`    "${filler}" -> "${replacement}"`);
      } else {
        skippedNoPool.push(filler);
        console.log(`    SKIP (no pool item): "${filler}"`);
      }
    }
    lines[i] = newLine;
  }
  
  if (replacementsMade > 0) {
    fs.writeFileSync(filepath, lines.join('\n'), 'utf-8');
  }
  
  if (skippedNoPool.length > 0) {
    console.log(`    WARNING: ${skippedNoPool.length} fillers could not be replaced (pool exhausted)`);
  }
  
  return replacementsMade;
}

function main() {
  const grades = process.argv.length > 2 
    ? process.argv.slice(2).map(Number) 
    : [6, 7];
  
  let total = 0;
  
  for (const grade of grades) {
    const gradeDir = path.join(GRADES_DIR, String(grade));
    if (!fs.existsSync(gradeDir)) continue;
    
    console.log(`\n${'='.repeat(60)}`);
    console.log(`Grade ${grade}`);
    console.log('='.repeat(60));
    
    const subjects = fs.readdirSync(gradeDir).sort();
    for (const subject of subjects) {
      const subjectDir = path.join(gradeDir, subject);
      if (!fs.statSync(subjectDir).isDirectory()) continue;
      const fp = path.join(subjectDir, 'index.ts');
      if (!fs.existsSync(fp)) continue;
      
      console.log(`\n  ${subject}:`);
      const count = fixFile(fp, grade, subject);
      total += count;
      console.log(`  -> ${count} replacements`);
    }
  }
  
  console.log(`\nTOTAL: ${total}`);
}

main();
