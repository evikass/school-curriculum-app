def escape(text: str) -> str:
    """XML-escape text."""
    return text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace('"', "&quot;")


def wrap_text(text: str, max_chars: int = 32) -> list:
    """Split text into lines no longer than max_chars."""
    words = text.split()
    lines = []
    current = ""
    for w in words:
        if current and len(current) + 1 + len(w) > max_chars:
            lines.append(current)
            current = w
        else:
            current = current + (" " if current else "") + w
    if current:
        lines.append(current)
    return lines


def build_svg(num: int, title: str, items: list) -> str:
    """Build one SVG string for a lesson."""

    # SVG header
    lines = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 500 350" width="500" height="350">',
    ]

    # Defs: gradient + filter
    lines.append('  <defs>')
    lines.append(f'    <linearGradient id="hdr" x1="0" y1="0" x2="1" y2="0">')
    lines.append(f'      <stop offset="0%" stop-color="{PRIMARY}"/>')
    lines.append(f'      <stop offset="100%" stop-color="{ACCENT}"/>')
    lines.append(f'    </linearGradient>')
    lines.append(f'    <linearGradient id="cardBg" x1="0" y1="0" x2="0" y2="1">')
    lines.append(f'      <stop offset="0%" stop-color="#FFFFFF"/>')
    lines.append(f'      <stop offset="100%" stop-color="#F1F8E9"/>')
    lines.append(f'    </linearGradient>')
    lines.append(f'    <filter id="shadow" x="-2%" y="-2%" width="104%" height="104%">')
    lines.append(f'      <feDropShadow dx="0" dy="1" stdDeviation="2" flood-color="{PRIMARY}" flood-opacity="0.12"/>')
    lines.append(f'    </filter>')
    lines.append('  </defs>')

    # Background
    lines.append(f'  <rect x="0" y="0" width="500" height="350" rx="12" ry="12" fill="{BG}"/>')

    # Outer border
    lines.append(f'  <rect x="1.5" y="1.5" width="497" height="347" rx="11" ry="11" fill="none" stroke="{PRIMARY}" stroke-width="2.5"/>')

    # Header bar
    lines.append(f'  <rect x="3" y="3" width="494" height="52" rx="9" ry="9" fill="url(#hdr)"/>')
    # Bottom corners of header are square visual (overlay)
    lines.append(f'  <rect x="3" y="35" width="494" height="20" fill="url(#hdr)"/>')

    # Header text - lesson number
    header_line = f"Экономика — Урок {num}"
    lines.append(f'  <text x="250" y="26" text-anchor="middle" font-family="Arial, sans-serif" font-size="13" fill="{WHITE}" opacity="0.85">{escape(header_line)}</text>')

    # Header text - title
    lines.append(f'  <text x="250" y="46" text-anchor="middle" font-family="Arial, sans-serif" font-size="17" font-weight="bold" fill="{WHITE}">{escape(title)}</text>')

    # Subtle decorative line below header
    lines.append(f'  <line x1="40" y1="58" x2="460" y2="58" stroke="{LIGHT}" stroke-width="0.5" opacity="0.6"/>')

    # Content area — 4 info cards
    card_top_start = 66
    card_height = 58
    card_gap = 5
    card_left = 16
    card_width = 468

    for i, (card_title, card_desc) in enumerate(items):
        cy = card_top_start + i * (card_height + card_gap)

        # Card background
        lines.append(f'  <rect x="{card_left}" y="{cy}" width="{card_width}" height="{card_height}" rx="6" ry="6" fill="url(#cardBg)" filter="url(#shadow)"/>')

        # Left accent stripe
        lines.append(f'  <rect x="{card_left}" y="{cy}" width="5" height="{card_height}" rx="3" ry="0" fill="{PRIMARY}"/>')
        # Make left stripe sharp on right side
        lines.append(f'  <rect x="{card_left + 2}" y="{cy}" width="3" height="{card_height}" fill="{PRIMARY}"/>')

        # Circle with number
        circle_cx = card_left + 28
        circle_cy_card = cy + 16
        lines.append(f'  <circle cx="{circle_cx}" cy="{circle_cy_card}" r="11" fill="{PRIMARY}" opacity="0.12"/>')
        lines.append(f'  <circle cx="{circle_cx}" cy="{circle_cy_card}" r="11" fill="none" stroke="{PRIMARY}" stroke-width="1" opacity="0.4"/>')
        lines.append(f'  <text x="{circle_cx}" y="{circle_cy_card + 4}" text-anchor="middle" font-family="Arial, sans-serif" font-size="12" font-weight="bold" fill="{PRIMARY}">{i + 1}</text>')

        # Card title
        text_x = card_left + 46
        lines.append(f'  <text x="{text_x}" y="{cy + 20}" font-family="Arial, sans-serif" font-size="13" font-weight="bold" fill="{DARK}">{escape(card_title)}</text>')

        # Card description — may need wrapping
        desc_lines = wrap_text(card_desc, max_chars=48)
        for j, dl in enumerate(desc_lines):
            dy = cy + 38 + j * 14
            if dy < cy + card_height - 2:
                lines.append(f'  <text x="{text_x}" y="{dy}" font-family="Arial, sans-serif" font-size="11" fill="#4E6E4E">{escape(dl)}</text>')

    # Footer separator
    footer_y = 330
    lines.append(f'  <line x1="20" y1="{footer_y}" x2="480" y2="{footer_y}" stroke="{PRIMARY}" stroke-width="0.5" opacity="0.25"/>')

    # Footer text
    lines.append(f'  <text x="250" y="{footer_y + 14}" text-anchor="middle" font-family="Arial, sans-serif" font-size="10" fill="{PRIMARY}" opacity="0.6">Экономика · 11 класс</text>')

    lines.append('</svg>')
    return "\n".join(lines)


def validate_xml(svg_str: str) -> bool:
    """Validate SVG as well-formed XML."""
    try:
        ET.fromstring(svg_str)
        return True
    except ET.ParseError as e:
        print(f"  XML validation error: {e}")
        return False


def main():
    os.makedirs(OUT, exist_ok=True)
    errors = 0
    for num, title, items in LESSONS:
        svg = build_svg(num, title, items)
        if not validate_xml(svg):
            errors += 1
            print(f"  ⚠ Lesson {num} has XML errors!")
        fname = f"lesson{num}.svg"
        fpath = os.path.join(OUT, fname)
        with open(fpath, "w", encoding="utf-8") as f:
            f.write(svg)
        print(f"  ✓ {fname} — {title}")
    print(f"\nDone: {len(LESSONS)} files, {errors} errors.")


if __name__ == "__main__":
    main()
