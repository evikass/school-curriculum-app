interface LiteraryWorkModalProps {
  workId: string | null
  isOpen: boolean
  onClose: () => void
}

export function LiteraryWorkModal({ workId, isOpen, onClose }: LiteraryWorkModalProps) {
  const work = workId ? literaryWorks[workId] : null

  useEffect(() => {
    const handleEscape = (e: KeyboardEvent) => {
      if (e.key === 'Escape') onClose()
    }
    window.addEventListener('keydown', handleEscape)
    return () => window.removeEventListener('keydown', handleEscape)
  }, [onClose])

  if (!work) return null

  return (
    <AnimatePresence>
      {isOpen && (
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          exit={{ opacity: 0 }}
          className="fixed inset-0 z-[100] flex items-center justify-center p-4 bg-black/80 backdrop-blur-sm"
          onClick={onClose}
        >
          <motion.div
            initial={{ scale: 0.9, opacity: 0, y: 50 }}
            animate={{ scale: 1, opacity: 1, y: 0 }}
            exit={{ scale: 0.9, opacity: 0, y: 50 }}
            className="bg-gradient-to-br from-amber-50 to-orange-50 dark:from-slate-900 dark:to-slate-800
                       rounded-3xl max-w-3xl w-full max-h-[90vh] overflow-hidden shadow-2xl border-2 border-amber-200/50"
            onClick={e => e.stopPropagation()}
          >
            {/* Header */}
            <div className="sticky top-0 bg-gradient-to-r from-amber-100 to-orange-100 dark:from-slate-800 dark:to-slate-700 
                            p-6 border-b border-amber-200/50">
              <div className="flex justify-between items-start">
                <div className="flex items-center gap-4">
                  <div className="p-3 bg-gradient-to-br from-amber-500 to-orange-500 rounded-2xl shadow-lg">
                    {work.type === 'былина' ? (
                      <Scroll className="w-8 h-8 text-white" />
                    ) : (
                      <BookOpen className="w-8 h-8 text-white" />
                    )}
                  </div>
                  <div>
                    <h2 className="text-2xl font-bold text-amber-900 dark:text-amber-100">
                      {work.title}
                    </h2>
                    {work.author && (
                      <p className="text-amber-700 dark:text-amber-300 flex items-center gap-2">
                        <Star className="w-4 h-4" />
                        {work.author}
                      </p>
                    )}
                    <span className="inline-block mt-2 px-3 py-1 bg-amber-200/50 dark:bg-amber-500/20 
                                     text-amber-800 dark:text-amber-200 rounded-full text-sm font-medium">
                      {work.type}
                    </span>
                  </div>
                </div>
                <button 
                  onClick={onClose}
                  className="p-2 hover:bg-amber-200/50 rounded-xl transition-colors"
                >
                  <X className="w-6 h-6 text-amber-700 dark:text-amber-300" />
                </button>
              </div>
            </div>
            
            {/* Content */}
            <div className="p-8 overflow-y-auto max-h-[calc(90vh-200px)]">
              <div className="prose prose-lg max-w-none">
                {work.content.split('\n\n').map((paragraph, idx) => (
                  <p 
                    key={idx} 
                    className="mb-4 text-gray-800 dark:text-gray-200 leading-relaxed text-lg indent-8"
                  >
                    {paragraph}
                  </p>
                ))}
              </div>
            </div>
            
            {/* Footer */}
            <div className="sticky bottom-0 bg-gradient-to-r from-amber-100 to-orange-100 dark:from-slate-800 dark:to-slate-700 
                            p-4 border-t border-amber-200/50">
              <div className="flex items-center justify-between">
                <p className="text-sm text-amber-700 dark:text-amber-300">
                  📖 Прочитайте внимательно и вернитесь к уроку
                </p>
                <button
                  onClick={onClose}
                  className="px-6 py-2 bg-gradient-to-r from-amber-500 to-orange-500 
                             hover:from-amber-400 hover:to-orange-400
                             text-white rounded-xl font-medium transition-all"
                >
                  Закрыть
                </button>
              </div>
            </div>
          </motion.div>
        </motion.div>
      )}
    </AnimatePresence>
  )
}

// Экспорт функции для открытия произведения
export function useLiteraryWork() {
  const [currentWork, setCurrentWork] = useState<string | null>(null)
  const [isModalOpen, setIsModalOpen] = useState(false)

  const openWork = (workId: string) => {
    setCurrentWork(workId)
    setIsModalOpen(true)
  }

  const closeWork = () => {
    setIsModalOpen(false)
    setTimeout(() => setCurrentWork(null), 300)
  }

  return { currentWork, isModalOpen, openWork, closeWork }
}

// Экспорт списка доступных произведений
export function getLiteraryWorkLink(workId: string, title: string): { id: string; title: string; available: boolean } {
  return {
    id: workId,
    title,
    available: !!literaryWorks[workId]
  }
}
