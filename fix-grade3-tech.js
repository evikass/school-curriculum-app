// ─── Validation ─────────────────────────────────────────────────────────────
const errors = [];
lessonIndex = 0;
for (const topic of data.lessons.detailedTopics) {
  for (const lesson of topic.lessons) {
    const idx = lessonIndex + 1;
    const title = lesson.title;

    // Check description length ≥800
    if (lesson.description.length < 800) {
      errors.push(`Lesson ${idx} "${title}": description is ${lesson.description.length} chars (need ≥800)`);
    }

    // Check keyPoints exists and has 4-5 items
    if (!lesson.keyPoints || lesson.keyPoints.length < 4 || lesson.keyPoints.length > 5) {
      errors.push(`Lesson ${idx} "${title}": keyPoints missing or wrong count (${lesson.keyPoints ? lesson.keyPoints.length : 0})`);
    }

    // Check examples ≥4
    if (!lesson.examples || lesson.examples.length < 4) {
      errors.push(`Lesson ${idx} "${title}": examples count ${lesson.examples ? lesson.examples.length : 0} (need ≥4)`);
    }

    // Check tests structure
    if (!lesson.tests) {
      errors.push(`Lesson ${idx} "${title}": tests missing`);
    } else {
      // quiz: 5 items
      if (!lesson.tests.quiz || lesson.tests.quiz.length !== 5) {
        errors.push(`Lesson ${idx} "${title}": quiz count ${lesson.tests.quiz ? lesson.tests.quiz.length : 0} (need 5)`);
      } else {
        for (let i = 0; i < lesson.tests.quiz.length; i++) {
          const q = lesson.tests.quiz[i];
          if (!q.question || !q.options || q.options.length !== 4 || q.correct === undefined) {
            errors.push(`Lesson ${idx} "${title}": quiz[${i}] invalid structure`);
          }
          if (q.correct < 0 || q.correct > 3) {
            errors.push(`Lesson ${idx} "${title}": quiz[${i}] correct index out of range`);
          }
        }
      }

      // find: 5 items
      if (!lesson.tests.find || lesson.tests.find.length !== 5) {
        errors.push(`Lesson ${idx} "${title}": find count ${lesson.tests.find ? lesson.tests.find.length : 0} (need 5)`);
      } else {
        for (let i = 0; i < lesson.tests.find.length; i++) {
          const f = lesson.tests.find[i];
          if (!f.text || !f.answer) {
            errors.push(`Lesson ${idx} "${title}": find[${i}] missing text or answer`);
          }
        }
      }

      // match: 5 items
      if (!lesson.tests.match || lesson.tests.match.length !== 5) {
        errors.push(`Lesson ${idx} "${title}": match count ${lesson.tests.match ? lesson.tests.match.length : 0} (need 5)`);
      } else {
        for (let i = 0; i < lesson.tests.match.length; i++) {
          const m = lesson.tests.match[i];
          if (!m.term || !m.definition) {
            errors.push(`Lesson ${idx} "${title}": match[${i}] missing term or definition`);
          }
        }
      }
    }

    // Verify preserved fields
    if (!lesson.facts || lesson.facts.length === 0) {
      errors.push(`Lesson ${idx} "${title}": facts missing or empty (should be preserved)`);
    }
    if (!lesson.tasks || lesson.tasks.length === 0) {
      errors.push(`Lesson ${idx} "${title}": tasks missing or empty (should be preserved)`);
    }
    if (!lesson.image) {
      errors.push(`Lesson ${idx} "${title}": image missing (should be preserved)`);
    }
    if (!lesson.content) {
      errors.push(`Lesson ${idx} "${title}": content missing (should be preserved)`);
    }

    lessonIndex++;
  }
}

