def escape_xml(text):
    """Escape special XML characters."""
    return (text
            .replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;"))


def generate_svg(lesson, subject_key):
    """Generate SVG content for a lesson with subject-specific theming."""
    num = lesson["num"]
    title = lesson["title"]
    content = lesson["content"]
    c = COLORS[subject_key]

    parts = []

    def w(s=""):
        parts.append(s)

    w(f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 500 350">')

    # ── Defs ──
    w('  <defs>')
    w(f'    <linearGradient id="hdr{subject_key}{num}" x1="0%" y1="0%" x2="100%" y2="0%">')
    w(f'      <stop offset="0%" style="stop-color:{c["primary"]};stop-opacity:1"/>')
    w(f'      <stop offset="100%" style="stop-color:{c["primary_light"]};stop-opacity:1"/>')
    w(f'    </linearGradient>')
    w(f'    <linearGradient id="bg{subject_key}{num}" x1="0%" y1="0%" x2="0%" y2="100%">')
    w(f'      <stop offset="0%" style="stop-color:{c["bg"]};stop-opacity:1"/>')
    w(f'      <stop offset="100%" style="stop-color:{c["bg_dark"]};stop-opacity:1"/>')
    w(f'    </linearGradient>')
    w(f'    <filter id="shadow{subject_key}{num}" x="-2%" y="-2%" width="104%" height="104%">')
    w(f'      <feDropShadow dx="1" dy="1" stdDeviation="2" flood-color="#000" flood-opacity="0.1"/>')
    w(f'    </filter>')
    w('  </defs>')

    # ── Background ──
    w(f'  <rect width="500" height="350" fill="url(#bg{subject_key}{num})"/>')

    # ── Rounded border ──
    w(f'  <rect x="2" y="2" width="496" height="346" rx="12" ry="12" fill="none" stroke="{c["primary"]}" stroke-width="2.5" opacity="0.6"/>')

    # ── Header background ──
    w(f'  <rect x="3" y="3" width="494" height="42" rx="10" ry="10" fill="url(#hdr{subject_key}{num})"/>')
    w(f'  <rect x="3" y="35" width="494" height="10" fill="url(#hdr{subject_key}{num})"/>')

    # ── Header text ──
    w(f'  <text x="250" y="20" font-family="Arial, sans-serif" font-size="13" font-weight="bold" fill="#FFFFFF" text-anchor="middle">{escape_xml(title)}</text>')
    w(f'  <text x="250" y="36" font-family="Arial, sans-serif" font-size="10" fill="{c["bg"]}" text-anchor="middle">{c["subject_label"]} — Урок {num}</text>')

    # ── Lesson number badge ──
    w(f'  <circle cx="28" cy="24" r="14" fill="#FFFFFF" opacity="0.25"/>')
    w(f'  <text x="28" y="28" font-family="Arial, sans-serif" font-size="13" font-weight="bold" fill="#FFFFFF" text-anchor="middle">{num}</text>')

    # ── Content area ──
    y_start = 52

    # LEFT COLUMN
    left_x = 12
    left_w = 230
    left_title = content["left_title"]
    left_items = content["left_items"]

    left_box_h = 12 + 18 + len(left_items) * 22 + 6
    w(f'  <rect x="{left_x}" y="{y_start}" width="{left_w}" height="{left_box_h}" rx="8" ry="8" fill="#FFFFFF" opacity="0.7" filter="url(#shadow{subject_key}{num})"/>')

    # Left title bar
    w(f'  <rect x="{left_x + 4}" y="{y_start + 3}" width="{left_w - 8}" height="18" rx="4" ry="4" fill="{c["primary"]}" opacity="0.15"/>')
    w(f'  <text x="{left_x + left_w // 2}" y="{y_start + 16}" font-family="Arial, sans-serif" font-size="10" font-weight="bold" fill="{c["primary"]}" text-anchor="middle">{escape_xml(left_title)}</text>')

    # Left items
    item_y = y_start + 28
    for item in left_items:
        if len(item) == 3:
            icon, text, color = item
        else:
            icon, text = item
            color = c["primary"]
        w(f'  <circle cx="{left_x + 14}" cy="{item_y - 3}" r="4" fill="{color}" opacity="0.8"/>')
        w(f'  <text x="{left_x + 24}" y="{item_y}" font-family="Arial, sans-serif" font-size="9" fill="{c["dark"]}">{escape_xml(text)}</text>')
        item_y += 22

    # RIGHT COLUMN
    right_x = 252
    right_w = 236
    right_title = content["right_title"]
    right_items = content["right_items"]

    right_box_h = 12 + 18 + len(right_items) * 22 + 6
    w(f'  <rect x="{right_x}" y="{y_start}" width="{right_w}" height="{right_box_h}" rx="8" ry="8" fill="#FFFFFF" opacity="0.7" filter="url(#shadow{subject_key}{num})"/>')

    # Right title bar
    w(f'  <rect x="{right_x + 4}" y="{y_start + 3}" width="{right_w - 8}" height="18" rx="4" ry="4" fill="{c["primary"]}" opacity="0.15"/>')
    w(f'  <text x="{right_x + right_w // 2}" y="{y_start + 16}" font-family="Arial, sans-serif" font-size="10" font-weight="bold" fill="{c["primary"]}" text-anchor="middle">{escape_xml(right_title)}</text>')

    # Right items
    item_y = y_start + 28
    for item in right_items:
        if len(item) == 3:
            icon, text, color = item
        else:
            icon, text = item
            color = c["primary"]
        if icon.isdigit():
            w(f'  <circle cx="{right_x + 14}" cy="{item_y - 3}" r="6" fill="{color}" opacity="0.2"/>')
            w(f'  <text x="{right_x + 14}" y="{item_y}" font-family="Arial, sans-serif" font-size="8" font-weight="bold" fill="{color}" text-anchor="middle">{icon}</text>')
        else:
            w(f'  <circle cx="{right_x + 14}" cy="{item_y - 3}" r="4" fill="{color}" opacity="0.8"/>')
        w(f'  <text x="{right_x + 26}" y="{item_y}" font-family="Arial, sans-serif" font-size="9" fill="{c["dark"]}">{escape_xml(text)}</text>')
        item_y += 22

    # BOTTOM SECTION
    bottom_title = content["bottom_title"]
    bottom_steps = content["bottom_steps"]

    bottom_y = y_start + max(left_box_h, right_box_h) + 8
    bottom_h = 14 + 16 + len(bottom_steps) * 18 + 8

    w(f'  <rect x="12" y="{bottom_y}" width="476" height="{bottom_h}" rx="8" ry="8" fill="#FFFFFF" opacity="0.7" filter="url(#shadow{subject_key}{num})"/>')

    # Bottom accent bar
    w(f'  <rect x="12" y="{bottom_y}" width="5" height="{bottom_h}" rx="2" ry="0" fill="{c["primary"]}" opacity="0.7"/>')

    # Bottom title
    w(f'  <text x="250" y="{bottom_y + 14}" font-family="Arial, sans-serif" font-size="10" font-weight="bold" fill="{c["primary_dark"]}" text-anchor="middle">{escape_xml(bottom_title)}</text>')

    # Bottom steps
    step_y = bottom_y + 30
    for step in bottom_steps:
        w(f'  <rect x="24" y="{step_y - 8}" width="452" height="15" rx="3" ry="3" fill="{c["primary"]}" opacity="0.06"/>')
        w(f'  <text x="30" y="{step_y}" font-family="Arial, sans-serif" font-size="9" fill="{c["dark"]}">{escape_xml(step)}</text>')
        step_y += 18

    # ── Decorative icon (subject-specific) ──
    icon_svg = get_decorative_icon(subject_key, num, c)
    w(icon_svg)

    # ── Footer ──
    footer_y = 338
    w(f'  <line x1="15" y1="{footer_y - 4}" x2="485" y2="{footer_y - 4}" stroke="{c["primary"]}" stroke-width="0.5" opacity="0.3"/>')
    w(f'  <text x="250" y="{footer_y}" font-family="Arial, sans-serif" font-size="8" fill="#9E9E9E" text-anchor="middle">{c["footer"]}</text>')

    w('</svg>')

    return "\n".join(parts)


