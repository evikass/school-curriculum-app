def validate_svg(filepath):
    """Validate SVG as proper XML."""
    try:
        tree = ET.parse(filepath)
        root = tree.getroot()
        if "svg" in root.tag.lower():
            return True, "Valid SVG XML"
        else:
            return False, f"Root element is not SVG: {root.tag}"
    except ET.ParseError as e:
        return False, f"XML Parse Error: {str(e)}"
    except Exception as e:
        return False, f"Error: {str(e)}"


def generate_subject(subject_key, lessons, output_dir):
    """Generate all SVGs for one subject, validate, and return results."""
    os.makedirs(output_dir, exist_ok=True)

    success = 0
    fail = 0
    results = []

    for lesson in lessons:
        num = lesson["num"]
        filename = f"lesson{num}.svg"
        filepath = os.path.join(output_dir, filename)

        svg_content = generate_svg(lesson, subject_key)

        with open(filepath, "w", encoding="utf-8") as f:
            f.write(svg_content)

        is_valid, message = validate_svg(filepath)

        if is_valid:
            success += 1
            file_size = os.path.getsize(filepath)
            results.append(f"  ✓ {filename} — VALID ({file_size} bytes)")
        else:
            fail += 1
            results.append(f"  ✗ {filename} — INVALID: {message}")

    return success, fail, results


