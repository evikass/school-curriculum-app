// ==================== ГЛАВНОЕ ПРИЛОЖЕНИЕ ====================
export default function App() {
  const [screen, setScreen] = useState('home');
  const [gradeId, setGradeId] = useState(null);
  const [subjectId, setSubjectId] = useState(null);
  const [topicIndex, setTopicIndex] = useState(null);
  const [lessonIndex, setLessonIndex] = useState(null);
  const [xp, setXp] = useState(0);
  const [completed, setCompleted] = useState([]);

  useEffect(() => {
    loadData();
  }, []);

  const loadData = async () => {
    try {
      const savedXp = await AsyncStorage.getItem('@inetshkola_xp');
      const savedCompleted = await AsyncStorage.getItem('@inetshkola_completed');
      if (savedXp) setXp(parseInt(savedXp));
      if (savedCompleted) setCompleted(JSON.parse(savedCompleted));
    } catch (e) {
      console.log('Load error:', e);
    }
  };

  const saveData = async (newXp, newCompleted) => {
    try {
      await AsyncStorage.setItem('@inetshkola_xp', String(newXp));
      await AsyncStorage.setItem('@inetshkola_completed', JSON.stringify(newCompleted));
    } catch (e) {
      console.log('Save error:', e);
    }
  };

  const completeLesson = () => {
    const key = `${gradeId}-${subjectId}-${topicIndex}-${lessonIndex}`;
    if (!completed.includes(key)) {
      const newCompleted = [...completed, key];
      const newXp = xp + 10;
      setCompleted(newCompleted);
      setXp(newXp);
      saveData(newXp, newCompleted);
    }
  };

  const level = Math.floor(Math.sqrt(xp / 100)) + 1;

  // Получить текущие данные
  const grade = gradeId !== null ? SCHOOL_DATA[gradeId] : null;
  const subject = grade && subjectId !== null ? grade.subjects.find(s => s.id === subjectId) : null;
  const topic = subject && topicIndex !== null ? subject.topics[topicIndex] : null;
  const lesson = topic && lessonIndex !== null ? topic.lessons[lessonIndex] : null;

  // ============ ГЛАВНЫЙ ЭКРАН ============
  if (screen === 'home') {
    return (
      <View style={styles.container}>
        <StatusBar barStyle="light-content" />
        <LinearGradient colors={['#1E1B4B', '#312E81', '#4C1D95']} style={styles.bg}>
          <SafeAreaView style={styles.safe}>
            <View style={styles.header}>
              <Text style={styles.title}>📚 ИНЕТШКОЛА</Text>
              <Text style={styles.subtitle}>Полная программа 0-11 класс • v1.5</Text>
            </View>
            
            <View style={styles.stats}>
              <Text style={styles.statText}>⭐ {xp} XP</Text>
              <Text style={styles.statText}>🎯 Уровень {level}</Text>
              <Text style={styles.statText}>✅ {completed.length} уроков</Text>
            </View>

            <ScrollView style={styles.scroll}>
              <View style={styles.grid}>
                {Object.entries(SCHOOL_DATA).map(([id, g]) => (
                  <TouchableOpacity
                    key={id}
                    style={[styles.card, { borderColor: g.color }]}
                    onPress={() => { setGradeId(parseInt(id)); setScreen('grade'); }}
                  >
                    <LinearGradient colors={[g.color, g.color + 'AA']} style={styles.cardBg}>
                      <Text style={styles.cardIcon}>{g.icon}</Text>
                      <Text style={styles.cardTitle}>{g.title}</Text>
                      <Text style={styles.cardSub}>{g.subjects.length} предметов</Text>
                    </LinearGradient>
                  </TouchableOpacity>
                ))}
              </View>
            </ScrollView>
          </SafeAreaView>
        </LinearGradient>
      </View>
    );
  }

  // ============ ЭКРАН КЛАССА ============
  if (screen === 'grade' && grade) {
    return (
      <View style={styles.container}>
        <StatusBar barStyle="light-content" />
        <LinearGradient colors={['#1E1B4B', '#312E81', '#4C1D95']} style={styles.bg}>
          <SafeAreaView style={styles.safe}>
            <TouchableOpacity style={styles.back} onPress={() => setScreen('home')}>
              <Text style={styles.backText}>← Назад</Text>
            </TouchableOpacity>
            
            <View style={styles.header}>
              <Text style={styles.pageTitle}>{grade.icon} {grade.title}</Text>
            </View>

            <ScrollView style={styles.scroll}>
              {grade.subjects.map((sub) => (
                <TouchableOpacity
                  key={sub.id}
                  style={[styles.item, { borderLeftColor: sub.color }]}
                  onPress={() => { setSubjectId(sub.id); setScreen('subject'); }}
                >
                  <Text style={styles.itemIcon}>{sub.icon}</Text>
                  <View style={styles.itemInfo}>
                    <Text style={styles.itemTitle}>{sub.title}</Text>
                    <Text style={styles.itemSub}>{sub.topics.length} тем</Text>
                  </View>
                  <Text style={styles.arrow}>→</Text>
                </TouchableOpacity>
              ))}
            </ScrollView>
          </SafeAreaView>
        </LinearGradient>
      </View>
    );
  }

  // ============ ЭКРАН ПРЕДМЕТА ============
  if (screen === 'subject' && subject) {
    return (
      <View style={styles.container}>
        <StatusBar barStyle="light-content" />
        <LinearGradient colors={['#1E1B4B', '#312E81', '#4C1D95']} style={styles.bg}>
          <SafeAreaView style={styles.safe}>
            <TouchableOpacity style={styles.back} onPress={() => setScreen('grade')}>
              <Text style={styles.backText}>← Назад</Text>
            </TouchableOpacity>
            
            <View style={styles.header}>
              <Text style={styles.pageTitle}>{subject.icon} {subject.title}</Text>
            </View>

            <ScrollView style={styles.scroll}>
              {subject.topics.map((t, ti) => (
                <View key={ti} style={styles.topicBox}>
                  <Text style={styles.topicTitle}>{t.title}</Text>
                  {t.lessons.map((l, li) => {
                    const key = `${gradeId}-${subjectId}-${ti}-${li}`;
                    const done = completed.includes(key);
                    return (
                      <TouchableOpacity
                        key={li}
                        style={[styles.lessonRow, done && styles.lessonDone]}
                        onPress={() => { setTopicIndex(ti); setLessonIndex(li); setScreen('lesson'); }}
                      >
                        <View style={[styles.numCircle, done && styles.numCircleDone]}>
                          {done ? <Text style={styles.checkMark}>✓</Text> : <Text style={styles.numText}>{li + 1}</Text>}
                        </View>
                        <Text style={styles.lessonTitle}>{l.title}</Text>
                        <Text style={styles.arrow}>→</Text>
                      </TouchableOpacity>
                    );
                  })}
                </View>
              ))}
            </ScrollView>
          </SafeAreaView>
        </LinearGradient>
      </View>
    );
  }

  // ============ ЭКРАН УРОКА ============
  if (screen === 'lesson' && lesson) {
    const key = `${gradeId}-${subjectId}-${topicIndex}-${lessonIndex}`;
    const done = completed.includes(key);

    return (
      <View style={styles.container}>
        <StatusBar barStyle="light-content" />
        <LinearGradient colors={['#1E1B4B', '#312E81', '#4C1D95']} style={styles.bg}>
          <SafeAreaView style={styles.safe}>
            <TouchableOpacity style={styles.back} onPress={() => setScreen('subject')}>
              <Text style={styles.backText}>← Назад</Text>
            </TouchableOpacity>
            
            <View style={styles.header}>
              <Text style={styles.pageTitle}>{lesson.title}</Text>
            </View>

            <ScrollView style={styles.scroll}>
              <View style={styles.contentBox}>
                <Text style={styles.contentHeader}>📖 Теория</Text>
                <Text style={styles.contentText}>{lesson.content}</Text>
              </View>

              {lesson.tasks && lesson.tasks.length > 0 && (
                <View style={styles.tasksBox}>
                  <Text style={styles.contentHeader}>✏️ Задания</Text>
                  {lesson.tasks.map((task, i) => (
                    <Text key={i} style={styles.taskText}>• {task}</Text>
                  ))}
                </View>
              )}

              <TouchableOpacity
                style={[styles.completeBtn, done && styles.completeBtnDone]}
                onPress={completeLesson}
              >
                <LinearGradient
                  colors={done ? ['#10B981', '#059669'] : ['#8B5CF6', '#7C3AED']}
                  style={styles.completeBtnBg}
                >
                  <Text style={styles.completeBtnText}>
                    {done ? '✓ Урок пройден' : 'Завершить урок (+10 XP)'}
                  </Text>
                </LinearGradient>
              </TouchableOpacity>
            </ScrollView>
          </SafeAreaView>
        </LinearGradient>
      </View>
    );
  }

  return null;
}

