function processSubject(filePath, lessonData, descExpansions, subjectName) {
  console.log(`\n=== Processing ${subjectName} ===`);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
  
  let totalLessons = 0;
  let addedKeyPoints = 0;
  let addedExamples = 0;
  let addedFacts = 0;
  let addedTests = 0;
  let expandedDescriptions = 0;

  data.lessons.detailedTopics.forEach(topic => {
    topic.lessons.forEach(lesson => {
      totalLessons++;
      const content = lessonData[lesson.title];
      if (!content) {
        console.warn(`  WARNING: No data for lesson "${lesson.title}"`);
        return;
      }

      // Add keyPoints
      if (!lesson.keyPoints || lesson.keyPoints.length < 4) {
        lesson.keyPoints = content.keyPoints;
        addedKeyPoints++;
      }

      // Add examples
      if (!lesson.examples || lesson.examples.length < 4) {
        lesson.examples = content.examples;
        addedExamples++;
      }

      // Add facts
      if (!lesson.facts || lesson.facts.length < 3) {
        lesson.facts = content.facts;
        addedFacts++;
      }

      // Add tests
      if (!lesson.tests || !lesson.tests.quiz || lesson.tests.quiz.length < 5) {
        lesson.tests = content.tests;
        addedTests++;
      }

      // Expand description if too short
      if (lesson.description.length < 800) {
        const expansion = descExpansions[lesson.title] || '';
        if (expansion) {
          lesson.description += expansion;
          lesson.theory = lesson.description; // Keep theory in sync
          expandedDescriptions++;
          console.log(`  Expanded description for "${lesson.title}" (${lesson.description.length} chars)`);
        } else {
          console.warn(`  WARNING: Description <800 chars but no expansion for "${lesson.title}" (${lesson.description.length} chars)`);
        }
      }
    });
  });

  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf-8');
  console.log(`\n${subjectName} results:`);
  console.log(`  Total lessons: ${totalLessons}`);
  console.log(`  Added keyPoints: ${addedKeyPoints}`);
  console.log(`  Added examples: ${addedExamples}`);
  console.log(`  Added facts: ${addedFacts}`);
  console.log(`  Added tests: ${addedTests}`);
  console.log(`  Expanded descriptions: ${expandedDescriptions}`);
  console.log(`  File written: ${filePath}`);

  return { totalLessons, addedKeyPoints, addedExamples, addedFacts, addedTests, expandedDescriptions };
}

