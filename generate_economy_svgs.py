def escape_xml(text):
    """Escape XML special characters."""
    return text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace('"', "&quot;").replace("'", "&apos;")


def generate_svg(lesson):
    """Generate SVG content for a lesson."""
    num = lesson["num"]
    title = lesson["title"]
    content = lesson["content"]

    lines = []
    lines.append('<?xml version="1.0" encoding="UTF-8"?>')
    lines.append(f'<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 500 350" width="500" height="350">')
    lines.append('  <defs>')
    lines.append('    <style>')
    lines.append(f'      .title {{ font: bold 14px "Segoe UI", Arial, sans-serif; fill: {WHITE}; }}')
    lines.append(f'      .lesson-num {{ font: bold 11px "Segoe UI", Arial, sans-serif; fill: {ACCENT2}; }}')
    lines.append(f'      .box-title {{ font: bold 11px "Segoe UI", Arial, sans-serif; fill: {PRIMARY}; }}')
    lines.append(f'      .box-text {{ font: 10px "Segoe UI", Arial, sans-serif; fill: {TEXT_DARK}; }}')
    lines.append(f'      .footer {{ font: 10px "Segoe UI", Arial, sans-serif; fill: {PRIMARY_LIGHT}; }}')
    lines.append('    </style>')
    lines.append('  </defs>')

    # Background
    lines.append(f'  <rect x="0" y="0" width="500" height="350" rx="12" ry="12" fill="{BG}" stroke="{PRIMARY}" stroke-width="2.5"/>')

    # Header bar
    lines.append(f'  <rect x="0" y="0" width="500" height="52" rx="12" ry="12" fill="{PRIMARY}"/>')
    lines.append(f'  <rect x="0" y="20" width="500" height="32" fill="{PRIMARY}"/>')

    # Title text
    title_x = 15
    lines.append(f'  <text x="{title_x}" y="24" class="title">{escape_xml(title)}</text>')
    lines.append(f'  <text x="{title_x}" y="42" class="lesson-num">Экономика — Урок {num}</text>')

    # Lesson number circle
    circle_cx = 475
    lines.append(f'  <circle cx="{circle_cx}" cy="26" r="18" fill="{PRIMARY_DARK}" stroke="{ACCENT2}" stroke-width="1.5"/>')
    lines.append(f'  <text x="{circle_cx}" y="31" text-anchor="middle" font-size="14" font-weight="bold" fill="{WHITE}">{num}</text>')

    # Content elements
    for elem in content:
        if elem["type"] == "box":
            x, y, w, h = elem["x"], elem["y"], elem["w"], elem["h"]
            # Box background
            lines.append(f'  <rect x="{x}" y="{y}" width="{w}" height="{h}" rx="6" ry="6" fill="{BOX_BG}" fill-opacity="0.9" stroke="{BOX_BORDER}" stroke-width="1.2"/>')
            # Top accent line
            lines.append(f'  <line x1="{x+6}" y1="{y}" x2="{x+w-6}" y2="{y}" stroke="{ACCENT}" stroke-width="2.5" stroke-linecap="round"/>')

            current_y = y + 14
            if elem.get("title"):
                lines.append(f'  <text x="{x+8}" y="{current_y}" class="box-title">{escape_xml(elem["title"])}</text>')
                current_y += 14

            for line_text in elem.get("lines", []):
                lines.append(f'  <text x="{x+8}" y="{current_y}" class="box-text">{escape_xml(line_text)}</text>')
                current_y += 13

        elif elem["type"] == "graph_desc":
            x, y, w, h = elem["x"], elem["y"], elem["w"], elem["h"]
            lines.append(f'  <rect x="{x}" y="{y}" width="{w}" height="{h}" rx="6" ry="6" fill="{BG_DARK}" fill-opacity="0.7" stroke="{BOX_BORDER}" stroke-width="1"/>')
            # Mini graph icon
            lines.append(f'  <line x1="{x+10}" y1="{y+h-10}" x2="{x+40}" y2="{y+15}" stroke="{PRIMARY}" stroke-width="1.5" stroke-dasharray="3,2"/>')
            lines.append(f'  <circle cx="{x+40}" cy="{y+15}" r="2.5" fill="{ACCENT}"/>')
            current_y = y + 14
            if elem.get("title"):
                lines.append(f'  <text x="{x+50}" y="{current_y}" class="box-title">{escape_xml(elem["title"])}</text>')
                current_y += 14
            for line_text in elem.get("lines", []):
                lines.append(f'  <text x="{x+50}" y="{current_y}" class="box-text">{escape_xml(line_text)}</text>')
                current_y += 13

        elif elem["type"] == "diagram_label":
            x, y = elem["x"], elem["y"]
            lines.append(f'  <text x="{x}" y="{y}" text-anchor="middle" font-size="11" font-weight="bold" fill="{PRIMARY}">{escape_xml(elem["text"])}</text>')

        elif elem["type"] == "arrow_box":
            x, y, w, h = elem["x"], elem["y"], elem["w"], elem["h"]
            lines.append(f'  <rect x="{x}" y="{y}" width="{w}" height="{h}" rx="5" ry="5" fill="{WHITE}" fill-opacity="0.8" stroke="{PRIMARY}" stroke-width="1"/>')
            text_x = x + w/2
            text_y = y + h/2 + 4
            lines.append(f'  <text x="{text_x}" y="{text_y}" text-anchor="middle" font-size="10" fill="{PRIMARY}">{escape_xml(elem["text"])}</text>')
            # Arrow
            if elem.get("arrow_to") == "right":
                ax1 = x + w + 2
                ax2 = x + w + 30
                ay = y + h/2
                lines.append(f'  <line x1="{ax1}" y1="{ay}" x2="{ax2}" y2="{ay}" stroke="{PRIMARY}" stroke-width="1.5"/>')
                lines.append(f'  <polygon points="{ax2},{ay-4} {ax2+7},{ay} {ax2},{ay+4}" fill="{PRIMARY}"/>')
            elif elem.get("arrow_to") == "left":
                ax1 = x - 2
                ax2 = x - 30
                ay = y + h/2
                lines.append(f'  <line x1="{ax1}" y1="{ay}" x2="{ax2}" y2="{ay}" stroke="{PRIMARY}" stroke-width="1.5"/>')
                lines.append(f'  <polygon points="{ax2},{ay-4} {ax2-7},{ay} {ax2},{ay+4}" fill="{PRIMARY}"/>')

    # Footer
    lines.append(f'  <rect x="0" y="325" width="500" height="25" rx="0" ry="0" fill="{PRIMARY}"/>')
    lines.append(f'  <rect x="0" y="325" width="500" height="25" rx="0" ry="0" fill="{PRIMARY}"/>')
    # Round bottom corners
    lines.append(f'  <rect x="0" y="330" width="500" height="20" rx="12" ry="12" fill="{PRIMARY}"/>')
    lines.append(f'  <rect x="0" y="330" width="500" height="10" fill="{PRIMARY}"/>')
    lines.append(f'  <text x="250" y="343" text-anchor="middle" class="footer">Экономика · 10 класс</text>')

    lines.append('</svg>')
    return '\n'.join(lines)


def validate_svg(svg_content):
    """Validate SVG as proper XML."""
    try:
        ET.fromstring(svg_content)
        return True, "Valid XML"
    except ET.ParseError as e:
        return False, str(e)


def main():
    print("=" * 60)
    print("Generating 32 Grade 10 Economics SVG files")
    print("=" * 60)

    success_count = 0
    fail_count = 0
    errors = []

    for lesson in lessons:
        num = lesson["num"]
        title = lesson["title"]
        filename = f"lesson{num}.svg"
        filepath = os.path.join(OUTPUT_DIR, filename)

        try:
            svg_content = generate_svg(lesson)

            # Validate
            is_valid, msg = validate_svg(svg_content)
            if not is_valid:
                errors.append(f"Lesson {num}: XML validation failed - {msg}")
                fail_count += 1
                print(f"  ✗ Lesson {num}: XML INVALID - {msg}")
                continue

            # Write file
            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(svg_content)

            file_size = os.path.getsize(filepath)
            success_count += 1
            print(f"  ✓ Lesson {num:2d}: {title[:45]:<45} ({file_size:,} bytes)")

        except Exception as e:
            errors.append(f"Lesson {num}: {str(e)}")
            fail_count += 1
            print(f"  ✗ Lesson {num}: ERROR - {str(e)}")

    print()
    print("=" * 60)
    print(f"RESULTS: {success_count} succeeded, {fail_count} failed out of 32")
    print("=" * 60)

    if errors:
        print("\nErrors:")
        for err in errors:
            print(f"  - {err}")

    # Double-check: validate all files on disk
    print("\n--- Re-validating all SVGs from disk ---")
    disk_valid = 0
    disk_invalid = 0
    for i in range(1, 33):
        filepath = os.path.join(OUTPUT_DIR, f"lesson{i}.svg")
        if not os.path.exists(filepath):
            print(f"  ✗ lesson{i}.svg: MISSING")
            disk_invalid += 1
            continue
        try:
            tree = ET.parse(filepath)
            root = tree.getroot()
            # Check it's SVG
            if root.tag.endswith('svg') or root.tag == 'svg':
                disk_valid += 1
            else:
                print(f"  ✗ lesson{i}.svg: Root tag is not <svg>")
                disk_invalid += 1
        except ET.ParseError as e:
            print(f"  ✗ lesson{i}.svg: Parse error - {e}")
            disk_invalid += 1

    print(f"\nDisk validation: {disk_valid} valid, {disk_invalid} invalid")
    return success_count


if __name__ == "__main__":
    main()
