function processSubject(filename, contentMap) {
  const filepath = path.join(BASE, filename);
  const data = JSON.parse(fs.readFileSync(filepath, 'utf8'));
  
  let lessonsProcessed = 0;
  let descriptionsExpanded = 0;
  
  data.lessons.detailedTopics.forEach(topic => {
    topic.lessons.forEach(lesson => {
      const content = contentMap[lesson.title];
      if (!content) {
        console.log(`  WARNING: No content for "${lesson.title}"`);
        return;
      }
      
      // Add keyPoints
      if (!lesson.keyPoints || lesson.keyPoints.length < 4) {
        lesson.keyPoints = content.keyPoints;
      }
      
      // Add examples
      if (!lesson.examples || lesson.examples.length < 4) {
        lesson.examples = content.examples;
      }
      
      // Add facts
      if (!lesson.facts || lesson.facts.length < 3) {
        lesson.facts = content.facts;
      }
      
      // Add tests
      if (!lesson.tests || !lesson.tests.quiz || lesson.tests.quiz.length < 5) {
        lesson.tests = content.tests;
      }
      
      // Expand description if needed
      if (content.description_expansion && lesson.description.length < 800) {
        lesson.description = content.description_expansion;
        descriptionsExpanded++;
      }
      
      lessonsProcessed++;
    });
  });
  
  fs.writeFileSync(filepath, JSON.stringify(data, null, 2), 'utf8');
  console.log(`${filename}: ${lessonsProcessed} lessons processed, ${descriptionsExpanded} descriptions expanded`);
  
  return { lessonsProcessed, descriptionsExpanded };
}

