function verifySubject(filename) {
  const filepath = path.join(BASE, filename);
  const data = JSON.parse(fs.readFileSync(filepath, 'utf8'));
  
  let totalLessons = 0;
  let issues = [];
  
  data.lessons.detailedTopics.forEach(topic => {
    topic.lessons.forEach(lesson => {
      totalLessons++;
      const title = lesson.title;
      
      if (!lesson.keyPoints || lesson.keyPoints.length < 4) {
        issues.push(`${title}: keyPoints < 4 (${lesson.keyPoints ? lesson.keyPoints.length : 0})`);
      }
      if (!lesson.examples || lesson.examples.length < 4) {
        issues.push(`${title}: examples < 4 (${lesson.examples ? lesson.examples.length : 0})`);
      }
      if (!lesson.facts || lesson.facts.length < 3) {
        issues.push(`${title}: facts < 3 (${lesson.facts ? lesson.facts.length : 0})`);
      }
      if (!lesson.tests || !lesson.tests.quiz || lesson.tests.quiz.length < 5) {
        issues.push(`${title}: quiz < 5`);
      }
      if (!lesson.tests || !lesson.tests.find || lesson.tests.find.length < 5) {
        issues.push(`${title}: find < 5`);
      }
      if (!lesson.tests || !lesson.tests.match || lesson.tests.match.length < 5) {
        issues.push(`${title}: match < 5`);
      }
      if (lesson.description.length < 800) {
        issues.push(`${title}: description < 800 chars (${lesson.description.length})`);
      }
    });
  });
  
  if (issues.length === 0) {
    console.log(`${filename}: ${totalLessons} lessons - ALL OK ✓`);
  } else {
    console.log(`${filename}: ${totalLessons} lessons - ${issues.length} issues:`);
    issues.forEach(i => console.log(`  - ${i}`));
  }
}

