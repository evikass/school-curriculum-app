// Build games
const data = JSON.parse(fs.readFileSync(FILE_PATH, 'utf-8'));

// Collect lesson examples and keyPoints for each lesson
const allLessons = [];
for (const topic of data.lessons.detailedTopics) {
  for (const lesson of topic.lessons) {
    allLessons.push(lesson);
  }
}

const newGames = [];
for (const game of games) {
  const lesson = allLessons.find(l => l.title.startsWith(`Урок ${game.lessonNum}:`));
  const tasks = game.questions.map((q, i) => ({
    type: 'quiz',
    question: q.q,
    options: [...q.options, 'Не знаю'],
    correctAnswer: q.correct,
    hint: q.hint,
    examples: lesson?.examples?.slice(0, 3) || [],
    keyPoints: lesson?.keyPoints?.slice(0, 3) || []
  }));

  newGames.push({
    title: `${game.title} ${EMOJI_MAP[game.lessonNum]}`,
    subject: 'Биология',
    icon: ICON_MAP[game.lessonNum] || 'BookOpenText',
    color: 'text-green-400',
    tasks: tasks
  });
}

data.games = [...newGames, ...(data.games || []).filter((g, i) => i >= newGames.length)];

fs.writeFileSync(FILE_PATH, JSON.stringify(data, null, 2), 'utf-8');
console.log(`Written ${newGames.length} games (lessons 1-10)`);
console.log('Total games:', data.games.length);
