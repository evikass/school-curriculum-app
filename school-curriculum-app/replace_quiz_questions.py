def main():
    # Read biology.json
    with open(BIOLOGY_JSON, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Build a map from lesson number to examples/keyPoints from detailedTopics
    lesson_data = {}
    lesson_idx = 0
    for topic in data['lessons']['detailedTopics']:
        for lesson in topic['lessons']:
            lesson_idx += 1
            lesson_data[lesson_idx] = {
                'examples': lesson.get('examples', []),
                'keyPoints': lesson.get('keyPoints', [])
            }

    # Replace tasks for games at index 10-59 (lessons 11-60)
    games_replaced = 0
    questions_created = 0

    for game_idx in range(10, 60):
        lesson_num = game_idx + 1  # games are 0-indexed, lessons are 1-indexed
        game = data['games'][game_idx]

        if lesson_num in QUIZ_DATA:
            new_tasks = []
            for q in QUIZ_DATA[lesson_num]:
                # Fix the question 26, #3 which has duplicate 'options' key
                task = {
                    'type': q['type'],
                    'question': q['question'],
                    'options': q['options'],
                    'correctAnswer': q['correctAnswer'],
                    'hint': q['hint'],
                    'examples': lesson_data.get(lesson_num, {}).get('examples', []),
                    'keyPoints': lesson_data.get(lesson_num, {}).get('keyPoints', [])
                }
                new_tasks.append(task)

            game['tasks'] = new_tasks
            games_replaced += 1
            questions_created += len(new_tasks)
        else:
            print(f"WARNING: No quiz data for lesson {lesson_num}")

    # Write back
    with open(BIOLOGY_JSON, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Done! Replaced {games_replaced} games with {questions_created} questions total.")
    print(f"Lessons 11-60 now have proper quiz questions.")


if __name__ == '__main__':
    main()
