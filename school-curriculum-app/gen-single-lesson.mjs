async function generateLesson(zai, lessonInfo) {
  const prompt = `Ты — опытный преподаватель биологии в российской школе. Создай ПОЛНЫЙ подробный урок биологии для 5 класса по учебнику Пасечника В.В. «Биология. Введение в биологию. 5 класс».

УРОК: ${lessonInfo.title}
ОПИСАНИЕ: ${lessonInfo.desc}

ТРЕБОВАНИЯ:
1. Content — ПОДРОБНЫЙ текст урока (минимум 2500 символов), написанный в Markdown, с заголовками ##, жирным шрифтом **, списками. Должен быть подробным и развёрнутым, как в учебнике.
2. Description — краткое описание урока (1-2 предложения)
3. Tasks — 4-5 учебных заданий по теме урока
4. Examples — 3 конкретных примера с пояснениями
5. Facts — 3-4 интересных факта
6. KeyPoints — 3-4 ключевых положения
7. Theory — то же содержимое что и Content

Ответь ТОЛЬКО валидным JSON, без markdown:
{
  "title": "Урок ${lessonInfo.num}: ${lessonInfo.title}",
  "description": "...",
  "tasks": ["...", "...", "...", "..."],
  "examples": ["...", "...", "..."],
  "facts": ["...", "...", "..."],
  "keyPoints": ["...", "...", "..."],
  "content": "## Заголовок\\n\\nПодробный текст урока...",
  "theory": "## Заголовок\\n\\nПодробный текст урока..."
}`;

  const completion = await zai.chat.completions.create({
    messages: [
      { role: 'system', content: 'Ты — опытный преподаватель биологии, автор подробных учебных материалов. Отвечай ТОЛЬКО валидным JSON без markdown-обёрток. Content и Theory должны содержать минимум 2500 символов.' },
      { role: 'user', content: prompt }
    ],
    temperature: 0.7,
    max_tokens: 4000,
  });

  let responseText = completion.choices[0]?.message?.content || '';
  responseText = responseText.replace(/```json\n?/g, '').replace(/```\n?/g, '').trim();

  const lesson = JSON.parse(responseText);

  if (!lesson.content || lesson.content.length < 1500) {
    throw new Error(`Content too short: ${lesson.content?.length || 0} chars`);
  }
  if (!lesson.theory || lesson.theory.length < 1500) {
    lesson.theory = lesson.content;
  }

  lesson.image = `/school-curriculum-app/images/lessons/grade5/biology/lesson${lessonInfo.num}.svg`;
  lesson.title = `Урок ${lessonInfo.num}: ${lessonInfo.title}`;

  return lesson;
}

async function main() {
  if (!LESSON_NUM || LESSON_NUM < 1 || LESSON_NUM > 24) {
    console.error('Usage: node gen-single-lesson.mjs <lesson_number 1-24>');
    process.exit(1);
  }

  const lessonInfo = CURRICULUM.find(l => l.num === LESSON_NUM);
  console.log(`Generating Lesson ${lessonInfo.num}: ${lessonInfo.title}...`);

  const zai = await ZAI.create();

  let lesson = null;
  let retries = 0;
  while (!lesson && retries < 5) {
    try {
      lesson = await generateLesson(zai, lessonInfo);
      console.log(`✓ Generated (${lesson.content.length} chars)`);
    } catch (error) {
      retries++;
      console.error(`Error: ${error.message}. Retry ${retries}...`);
      await new Promise(resolve => setTimeout(resolve, retries * 15000));
    }
  }

  if (!lesson) {
    console.error('✗ FAILED after 5 retries');
    process.exit(1);
  }

  // Save to file
  const data = JSON.parse(fs.readFileSync(FILE_PATH, 'utf-8'));

  // Check if we need to rebuild the lessons structure
  if (!data.lessons || !data.lessons.detailedTopics) {
    data.lessons = { title: 'Биология', icon: 'Leaf', color: 'text-green-400', topics: [], detailedTopics: [] };
  }

  // Find or create topic
  let topicObj = data.lessons.detailedTopics.find(t => t.topic === lessonInfo.topic);
  if (!topicObj) {
    topicObj = { topic: lessonInfo.topic, lessons: [] };
    data.lessons.detailedTopics.push(topicObj);
    if (!data.lessons.topics.includes(lessonInfo.topic)) {
      data.lessons.topics.push(lessonInfo.topic);
    }
  }

  // Check if lesson already exists in topic
  const existingIdx = topicObj.lessons.findIndex(l => l.title.startsWith(`Урок ${lessonInfo.num}:`));
  if (existingIdx >= 0) {
    topicObj.lessons[existingIdx] = lesson;
    console.log(`Replaced existing lesson ${lessonInfo.num}`);
  } else {
    topicObj.lessons.push(lesson);
    // Sort lessons by number
    topicObj.lessons.sort((a, b) => {
      const numA = parseInt(a.title.match(/Урок\s+(\d+)/)?.[1] || '0');
      const numB = parseInt(b.title.match(/Урок\s+(\d+)/)?.[1] || '0');
      return numA - numB;
    });
  }

  // Ensure topics are in correct order
  const topicOrder = [
    "Биология — наука о живой природе",
    "Клетка — основа строения и жизнедеятельности организмов",
    "Бактерии",
    "Грибы",
    "Растения"
  ];
  data.lessons.detailedTopics.sort((a, b) => topicOrder.indexOf(a.topic) - topicOrder.indexOf(b.topic));
  data.lessons.topics = data.lessons.detailedTopics.map(t => t.topic);

  if (!data.games) data.games = [];

  fs.writeFileSync(FILE_PATH, JSON.stringify(data, null, 2), 'utf-8');
  console.log(`✓ Saved lesson ${lessonInfo.num} to biology.json`);
}

main().catch(err => {
  console.error('Fatal error:', err);
  process.exit(1);
});
