const existingData = JSON.parse(fs.readFileSync(FILE_PATH, 'utf-8'));

const topicMap = new Map();
for (const l of lessons) {
  if (!topicMap.has(l.topic)) topicMap.set(l.topic, []);
  topicMap.get(l.topic).push({
    title: `Урок ${l.num}: ${l.title}`,
    description: l.description,
    tasks: l.tasks,
    examples: l.examples,
    facts: l.facts,
    keyPoints: l.keyPoints,
    content: l.content,
    theory: l.theory,
    image: `/school-curriculum-app/images/lessons/grade5/biology/lesson${l.num}.svg`
  });
}

const topicOrder = [
  "Биология — наука о живой природе",
  "Клетка — основа строения и жизнедеятельности организмов",
  "Бактерии",
  "Грибы",
  "Растения"
];

for (const [topic, topicLessons] of topicMap) {
  let topicObj = existingData.lessons.detailedTopics.find(t => t.topic === topic);
  if (!topicObj) {
    topicObj = { topic, lessons: [] };
    existingData.lessons.detailedTopics.push(topicObj);
  }
  for (const lesson of topicLessons) {
    const existingIdx = topicObj.lessons.findIndex(l => l.title === lesson.title);
    if (existingIdx >= 0) {
      topicObj.lessons[existingIdx] = lesson;
    } else {
      topicObj.lessons.push(lesson);
    }
  }
  topicObj.lessons.sort((a, b) => {
    const numA = parseInt(a.title.match(/Урок\s+(\d+)/)?.[1] || '0');
    const numB = parseInt(b.title.match(/Урок\s+(\d+)/)?.[1] || '0');
    return numA - numB;
  });
}

existingData.lessons.detailedTopics.sort((a, b) => topicOrder.indexOf(a.topic) - topicOrder.indexOf(b.topic));
existingData.lessons.topics = existingData.lessons.detailedTopics.map(t => t.topic);

fs.writeFileSync(FILE_PATH, JSON.stringify(existingData, null, 2), 'utf-8');

