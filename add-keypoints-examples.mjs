// Process files that use createLesson helper
function processCreateLessonFile(filePath, subjectName, data) {
  console.log(`Processing ${subjectName}...`);
  let content = fs.readFileSync(filePath, 'utf-8');
  
  // Check current helper pattern
  const oldHelperPattern = /const createLesson = \(title: string, description: string, tasks: string\[\]\) => \(\{\s*title, description, tasks\s*\}\)/;
  
  if (oldHelperPattern.test(content)) {
    // Replace helper
    content = content.replace(
      oldHelperPattern,
      'const createLesson = (title: string, description: string, tasks: string[], examples?: string[], keyPoints?: string[]) => ({ title, description, tasks, examples, keyPoints })'
    );
    console.log(`  Updated createLesson helper`);
  }
  
  // For each lesson, find the createLesson call and add examples/keyPoints
  const lessonEntries = Object.entries(data.lessons);
  let modified = 0;
  
  for (const [lessonTitle, lessonData] of lessonEntries) {
    // Find the createLesson call for this lesson - match the title
    const titlePattern = new RegExp(`createLesson\\("([^"]*${escapeRegex(lessonTitle)}[^"]*)",`);
    const match = content.match(titlePattern);
    
    if (!match) {
      console.log(`  WARNING: Could not find lesson "${lessonTitle}"`);
      continue;
    }
    
    // Find the closing of this createLesson call
    // We need to find the tasks array closing ]) and add our data before it
    // Strategy: find the createLesson with this title, then find the next createLesson or end of array
    
    const startIdx = content.indexOf(match[0]);
    // Find the tasks array - it ends with ]),
    // We need to find the matching ]) that closes createLesson
    
    let searchStart = startIdx + match[0].length;
    let depth = 1; // We're inside createLesson( already
    let closeIdx = -1;
    
    for (let i = searchStart; i < content.length; i++) {
      if (content[i] === '(' || content[i] === '[') depth++;
      else if (content[i] === ')' || content[i] === ']') {
        depth--;
        if (depth === 0) {
          closeIdx = i;
          break;
        }
      }
    }
    
    if (closeIdx === -1) {
      console.log(`  WARNING: Could not find closing of lesson "${lessonTitle}"`);
      continue;
    }
    
    // Check if this lesson already has examples/keyPoints
    const callContent = content.substring(startIdx, closeIdx + 1);
    if (callContent.includes('examples:') || callContent.includes('keyPoints:')) {
      console.log(`  SKIP: "${lessonTitle}" already has examples/keyPoints`);
      continue;
    }
    
    // Insert before the closing )
    const insertPos = closeIdx;
    const examplesStr = JSON.stringify(lessonData.examples, null, 16).replace(/"/g, "'").replace(/\n/g, '\n          ');
    const keyPointsStr = JSON.stringify(lessonData.keyPoints, null, 16).replace(/"/g, "'").replace(/\n/g, '\n          ');
    
    const insertion = `\n        ${examplesStr},\n        ${keyPointsStr}\n      `;
    content = content.substring(0, insertPos) + insertion + content.substring(insertPos);
    modified++;
  }
  
  fs.writeFileSync(filePath, content, 'utf-8');
  console.log(`  Modified ${modified} lessons`);
  return modified;
}

function escapeRegex(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// Process files that already have examples/keyPoints in helper (like biology's L helper)
// but we just need to verify they exist
function checkAlreadyDone(filePath, subjectName) {
  const content = fs.readFileSync(filePath, 'utf-8');
  const hasExamples = content.includes('examples:');
  const hasKeyPoints = content.includes('keyPoints:');
  console.log(`${subjectName}: examples=${hasExamples}, keyPoints=${hasKeyPoints} - ${hasExamples && hasKeyPoints ? 'DONE' : 'NEEDS WORK'}`);
  return hasExamples && hasKeyPoints;
}

