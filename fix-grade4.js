function processFile(filePath, lessonData) {
  console.log(`\n📄 Processing: ${filePath}`);
  const raw = fs.readFileSync(filePath, 'utf8');
  const data = JSON.parse(raw);
  
  const detailedTopics = data.lessons.detailedTopics;
  let lessonIndex = 0;
  let updatedCount = 0;
  
  for (const topic of detailedTopics) {
    for (const lesson of topic.lessons) {
      const lessonUpdate = lessonData[lessonIndex];
      if (!lessonUpdate) {
        console.log(`  ⚠️ No data for lesson index ${lessonIndex} (${lesson.title})`);
        lessonIndex++;
        continue;
      }
      
      // 1. REPLACE description
      const oldDesc = lesson.description;
      lesson.description = lessonUpdate.description;
      
      // 2. ADD keyPoints
      lesson.keyPoints = lessonUpdate.keyPoints;
      
      // 3. ADD 1-2 more examples
      if (lessonUpdate.examples && lesson.examples) {
        const existingCount = lesson.examples.length;
        const neededExtra = Math.max(0, 4 - existingCount);
        const extraExamples = lessonUpdate.examples.slice(existingCount, existingCount + neededExtra + 1);
        lesson.examples = [...lesson.examples, ...extraExamples];
      }
      
      // 4. ADD tests
      lesson.tests = lessonUpdate.tests;
      
      // Verify preservation
      const preserved = ['tasks', 'theory', 'facts', 'image', 'content'];
      for (const field of preserved) {
        if (lesson[field] === undefined) {
          console.log(`  ⚠️ Field ${field} missing in lesson ${lessonIndex}`);
        }
      }
      
      // Verify description length
      const descLen = lesson.description.length;
      const status = descLen >= 800 ? '✅' : '❌';
      console.log(`  ${status} Lesson ${lessonIndex + 1}: "${lesson.title}" — desc: ${descLen} chars, keyPoints: ${lesson.keyPoints.length}, examples: ${lesson.examples.length}, quiz: ${lesson.tests.quiz.length}, find: ${lesson.tests.find.length}, match: ${lesson.tests.match.length}`);
      
      updatedCount++;
      lessonIndex++;
    }
  }
  
  // Write back
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
  console.log(`  ✅ Written ${updatedCount} lessons to ${filePath}`);
  return updatedCount;
}

