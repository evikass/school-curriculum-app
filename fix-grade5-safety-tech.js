// ============ PROCESSING ============
function processFile(filename, lessonDataMap) {
  const filepath = path.join(BASE, filename);
  const data = JSON.parse(fs.readFileSync(filepath, 'utf8'));
  
  let stats = { totalLessons: 0, addedKeyPoints: 0, addedExamples: 0, addedFacts: 0, addedTests: 0, expandedDescs: 0 };
  
  data.lessons.detailedTopics.forEach(topic => {
    topic.lessons.forEach(lesson => {
      stats.totalLessons++;
      const lessonData = lessonDataMap[lesson.title];
      if (!lessonData) {
        console.log(`WARNING: No data for lesson "${lesson.title}"`);
        return;
      }
      
      // Add keyPoints
      if (!lesson.keyPoints || lesson.keyPoints.length < 4) {
        lesson.keyPoints = lessonData.keyPoints;
        stats.addedKeyPoints++;
      }
      
      // Add examples
      if (!lesson.examples || lesson.examples.length < 4) {
        lesson.examples = lessonData.examples;
        stats.addedExamples++;
      }
      
      // Add facts
      if (!lesson.facts || lesson.facts.length < 3) {
        lesson.facts = lessonData.facts;
        stats.addedFacts++;
      }
      
      // Add tests
      if (!lesson.tests || !lesson.tests.quiz || lesson.tests.quiz.length < 5) {
        lesson.tests = {
          quiz: lessonData.quiz,
          find: lessonData.find,
          match: lessonData.match
        };
        stats.addedTests++;
      }
      
      // Expand description if < 800 chars
      if (lesson.description.length < 800) {
        const extraContent = `\n\n### 📌 Дополнительная информация\n\n${lessonData.keyPoints.map((kp, i) => `${i+1}. ${kp}`).join('\n')}\n\n**Интересные факты:**\n${lessonData.facts.map(f => `- ${f}`).join('\n')}`;
        lesson.description += extraContent;
        stats.expandedDescs++;
      }
    });
  });
  
  fs.writeFileSync(filepath, JSON.stringify(data, null, 2), 'utf8');
  console.log(`\n✅ ${filename} processed:`);
  console.log(`   Total lessons: ${stats.totalLessons}`);
  console.log(`   Added keyPoints: ${stats.addedKeyPoints}`);
  console.log(`   Added examples: ${stats.addedExamples}`);
  console.log(`   Added facts: ${stats.addedFacts}`);
  console.log(`   Added tests: ${stats.addedTests}`);
  console.log(`   Expanded descriptions: ${stats.expandedDescs}`);
  
  return stats;
}

