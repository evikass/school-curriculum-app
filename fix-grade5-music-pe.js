function fixSubjectFile(subjectKey, data) {
  const filePath = path.join(DATA_DIR, `${subjectKey}.json`);
  console.log(`\n===== Processing ${subjectKey}.json =====`);
  
  const raw = fs.readFileSync(filePath, 'utf-8');
  const json = JSON.parse(raw);
  
  let lessonCount = 0;
  let modifiedCount = 0;
  
  for (const topic of json.lessons.detailedTopics) {
    for (const lesson of topic.lessons) {
      lessonCount++;
      const lessonTitle = lesson.title;
      
      // Find matching data entry
      let entry = null;
      if (data[lessonTitle]) {
        entry = data[lessonTitle];
      } else {
        // Try partial match
        for (const key of Object.keys(data)) {
          if (lessonTitle.includes(key.substring(key.indexOf(':') + 1).trim()) || 
              key.includes(lessonTitle.substring(lessonTitle.indexOf(':') + 1).trim())) {
            entry = data[key];
            break;
          }
        }
      }
      
      if (!entry) {
        console.log(`  WARNING: No data for lesson "${lessonTitle}"`);
        continue;
      }
      
      // a. ADD keyPoints
      if (!lesson.keyPoints) {
        lesson.keyPoints = entry.keyPoints;
        console.log(`  + keyPoints added to "${lessonTitle}"`);
      } else {
        console.log(`  = keyPoints already exist in "${lessonTitle}"`);
      }
      
      // b. ADD extra examples
      if (lesson.examples.length < 4) {
        const needed = 4 - lesson.examples.length;
        const toAdd = entry.extraExamples.slice(0, needed);
        lesson.examples = [...lesson.examples, ...toAdd];
        console.log(`  + Added ${toAdd.length} examples to "${lessonTitle}" (now ${lesson.examples.length})`);
      } else {
        console.log(`  = Examples already ≥4 in "${lessonTitle}" (${lesson.examples.length})`);
      }
      
      // c. KEEP existing facts (do NOT modify)
      
      // d. ADD tests
      if (!lesson.tests) {
        lesson.tests = entry.tests;
        console.log(`  + tests added to "${lessonTitle}" (quiz: ${entry.tests.quiz.length}, find: ${entry.tests.find.length}, match: ${entry.tests.match.length})`);
      } else {
        console.log(`  = tests already exist in "${lessonTitle}"`);
      }
      
      modifiedCount++;
    }
  }
  
  console.log(`\nTotal lessons: ${lessonCount}, Modified: ${modifiedCount}`);
  
  // Write back
  fs.writeFileSync(filePath, JSON.stringify(json, null, 2), 'utf-8');
  console.log(`File ${subjectKey}.json written successfully.`);
  
  return { lessonCount, modifiedCount };
}

